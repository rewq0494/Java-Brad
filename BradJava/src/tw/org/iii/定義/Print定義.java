package tw.org.iii.定義;

public class Print定義 {

	public static void main(String[] args) {
		/*
		 * println() 和 printf() 都是 Java 中用於輸出資料的方法。
		 
		  println() 是一個用於輸出字串的方法，並且會自動換行。它的使用方式如下：
		  System.out.println("Hello, World!"); 
		  
		  
		  printf()則是一個用於格式化輸出的方法，它可以指定輸出的格式，並且不會自動換行。它的使用方式如下：
		  System.out.printf("Hello, %s! Your age is %d.\n", name, age);
		  其中 %s 和 %d 是格式化字串，分別表示要輸出的字串和整數。在方法的參數列表中，
		  用逗號分隔的參數將會按照格式化字串中的順序進行替換。最後的 \n是換行符號，用於手動換行。
		  
		 */
	}

}
